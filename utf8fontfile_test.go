func TestUTF8CutFont(t *testing.T) {
	in := testFontData
	out := UTF8CutFont(in, "中")
	if len(out) == 0 {
		t.Fatal()
	}
	tf, err := ioutil.TempFile(os.TempDir(), "mcuts*.ttf")
	if err != nil {
		t.Fatal(err)
	}
	fn := tf.Name()
	t.Log(fn)
	defer os.Remove(fn)
	ioutil.WriteFile(fn, out, 0644)
	ttf, err := TtfParse(fn)
	if err != nil {
		t.Fatal(err)
	}
	chars := ttf.Chars
	if l := len(chars); l != 1 {
		t.Fatal(l)
	}
	if _, ok := chars[uint16(rune('中'))]; !ok {
		t.Fatal(chars)
	}
}
